#Deklarasi Variabel
angka_pertama = int(input("Masukkan angka pertama : "))
angka_kedua = int(input("Masukkan angka kedua : "))

#Proses
print("Penjumlahan => ",angka_pertama,"+",angka_kedua,"=",(angka_pertama+angka_kedua))
print("Pengurangan => ",angka_pertama,"-",angka_kedua,"=",(angka_pertama-angka_kedua))
print("Perkalian => ",angka_pertama,"*",angka_kedua,"=",(angka_pertama*angka_kedua))
print("Pembagian => ",angka_pertama,"/",angka_kedua,"=",(angka_pertama/angka_kedua))
print("Pembagian Dibulatkan ke bawah => ",angka_pertama,"//",angka_kedua,"=",(angka_pertama//angka_kedua))
print("Modulus => ",angka_pertama,"%",angka_kedua,"=",(angka_pertama%angka_kedua))
print("Perpangkatan => ",angka_pertama,"**",angka_kedua,"=",(angka_pertama**angka_kedua))


